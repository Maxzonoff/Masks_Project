def test_count_by_category(transaction):
    assert count_by_category(transaction, []) == {}


def test_not_categories(transaction):
    categories = ["qwerty"]
    assert count_by_category(transaction, categories) == {}


def test_empty_categories(transaction):
    assert count_by_category(transaction, []) == {}


def test_count_by_category_ok(transaction):
    categories = ["Перевод организации", "Перевод со счета на счет", "Перевод с карты на карту"]
    assert count_by_category(transaction, categories) == {
        "Перевод организации": 2,
        "Перевод со счета на счет": 2,
        "Перевод с карты на карту": 1,
    }
